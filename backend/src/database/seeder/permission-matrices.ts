/**
 * Permission matrices for the standard role set. Extracted from
 * {@link PermissionSeeder} to keep the seeder file focused on logic and
 * make the per-role data easier to review/diff.
 *
 * Each role lists, per CRUD-ish action, the entity handles for which the
 * role should be granted the permission (subject to the entity's own
 * `canRead/canInsert/...` flags).
 */
export type PermissionMatrix = {
  read: Set<string>;
  insert: Set<string>;
  update: Set<string>;
  delete: Set<string>;
  show: Set<string>;
};

export type PermissionMatrixInput = Record<keyof PermissionMatrix, string[]>;

export function createMatrix(input: PermissionMatrixInput): PermissionMatrix {
  return {
    read: new Set(input.read),
    insert: new Set(input.insert),
    update: new Set(input.update),
    delete: new Set(input.delete),
    show: new Set(input.show),
  };
}

